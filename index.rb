# 1.Rubyの出力方法
# Rubyの出力の命令は「puts」 コメントアウトは先頭に「#」
# 文字列はシングルクォーテーション「''」もしくはダブルコーテーション「""」で囲む
puts "こんにちは、Ruby"
# →こんにちは、Ruby



# 2.数値・四則演算
puts 37
# →37

puts "2 + 9"
# →2 + 9

puts 2 + 9
# →11

puts 13 * 9
# →117

puts 32 / 8
# →4

puts 18 % 5
# →3(余り)



# 3.文字・数値の連結
puts "私は" + "にんじゃわんこです"
# →私はにんじゃわんこです

puts "38" + "19"
# →3819